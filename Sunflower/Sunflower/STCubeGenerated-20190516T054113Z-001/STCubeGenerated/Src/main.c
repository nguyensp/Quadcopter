		acc_z = myAccelRaw.z;
	
		gyro_x = myGyroRaw.x;
		gyro_y = myGyroRaw.y;
		gyro_z = myGyroRaw.z;
		
		//Motor Output
		htim1.Instance->CCR1 = dutyCycle;
		htim1.Instance->CCR2 = dutyCycle;
		htim1.Instance->CCR3 = dutyCycle;
		htim1.Instance->CCR4 = dutyCycle;
		dutyCycle+= 2;
		if(dutyCycle == 30) 
      dutyCycle=28;
		HAL_Delay(1000);
